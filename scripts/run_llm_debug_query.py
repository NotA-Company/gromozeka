async def runQueryWithResult(
    model: AbstractModel,
    modelName: str,
    messages: List[ModelMessage],
) -> tuple[ModelResultStatus, str]:
    """Run the LLM query and print results.

    Args:
        model: The AbstractModel instance to query.
        modelName: The name of the model (for display).
        messages: List of ModelMessage objects to send.

    Returns:
        Tuple of (status, resultText).
    """
    from lib.ai.models import ModelRunResult  # noqa: F401

    print(f"Running query with model: {_col(modelName, _ANSI_BLUE)}\n")
    print(f"Message count: {len(messages)}\n")

    # Show first message (usually system prompt) for context
    if messages and messages[0].role == "system":
        systemContent = messages[0].content
        preview = systemContent[:200] + "..." if len(systemContent) > 200 else systemContent
        formattedPreview = _col(preview, _ANSI_GREEN)
        print("System prompt (first 200 chars):\n")
        print(f'  "{formattedPreview}"')

    # Run the query
    startTime = time.time()
    result: ModelRunResult = await model.generateText(messages)
    elapsedTime = time.time() - startTime

    # Store resultText before potentially printing
    resultText = result.resultText or ""

    # Print the result
    print("=" * 80)
    print(_col("RESULT", _ANSI_BLUE))
    print("=" * 80)
    print(
        f"Status: {_col(result.status.name, _ANSI_GREEN if result.status == ModelResultStatus.FINAL else _ANSI_RED)}\n"
    )

    if result.error:
        print(f"Error: {_col(str(result.error), _ANSI_RED)}\n")

    if resultText:
        print("Response:")
        print("-" * 80)
        print(resultText)
        print("-" * 80)
        print()

    print(_col("DETAILS:", _ANSI_YELLOW))
    print(f"  Input tokens:  {result.inputTokens or 'N/A'}")
    print(f"  Output tokens: {result.outputTokens or 'N/A'}")
    print(f"  Total tokens:  {result.totalTokens or 'N/A'}")
    print(f"  Tool calls:    {len(result.toolCalls)}")
    print(f"  Elapsed time:  {elapsedTime:.2f}s")
    if result.toolCalls:
        for tc in result.toolCalls:
            print(f"    - {tc.name}({tc.parameters})")
    print()

    return result.status, resultText


def compareResponses(originalResponse: str, newResponse: str) -> None:
    """Print a side-by-side comparison of original and new responses.

    Args:
        originalResponse: The original response from the log.
        newResponse: The new response from the LLM.
    """
    print("=" * 80)
    print(_col("COMPARISON", _ANSI_BLUE))
    print("=" * 80)

    # Truncate very long responses for display
    maxDisplayLen = 500

    origPreview = originalResponse[:maxDisplayLen] + ("..." if len(originalResponse) > maxDisplayLen else "")
    newPreview = newResponse[:maxDisplayLen] + ("..." if len(newResponse) > maxDisplayLen else "")

    print("\nOriginal response:\n")
    print("-" * 40)
    print(origPreview)
    print("-" * 40)

    print("\nNew response:\n")
    print("-" * 40)
    print(newPreview)
    print("-" * 40)

    # Check if responses are identical
    if originalResponse == newResponse:
        print(f"\n{_col('Responses are identical', _ANSI_GREEN)}")
    else:
        lengthDiff = abs(len(originalResponse) - len(newResponse))
        print(f"\n{_col(f'Responses differ (length diff: {lengthDiff} chars)', _ANSI_YELLOW)}")


async def main() -> int:
    """Main entry point for the script.

    Returns:
        Exit code (0 for success, 1 for error).
    """
    args = _parseArgs()

    # Load the human-readable JSON/YAML file BEFORE initializing ConfigManager
    # because config loading may change the working directory
    print(f"Loading log from: {_col(args.inputFile, _ANSI_BLUE)}")
    try:
        entries = loadReadableFile(args.inputFile)
    except Exception as e:
        print(f"Error loading file: {_col(str(e), _ANSI_RED)}", file=sys.stderr)
        return 1

    if not entries:
        print("No log entries found in file.", file=sys.stderr)
        return 1

    # For simplicity, use the first entry
    entry = entries[0]

    print(f"Found {len(entries)} log entr{'y' if len(entries) == 1 else 'ies'}, using first entry\n")

    # Load config (this may change working directory)
    configDirs: List[str] = args.configDirs if args.configDirs else ["configs/00-defaults", "configs/local"]

    print(f"Loading configs from: {', '.join(configDirs)}")
    configManager = ConfigManager(
        configPath="config.toml",
        configDirs=configDirs,
        dotEnvFile=args.dotenv_file,
    )

    # Initialize LLM manager
    llmManager = LLMManager(configManager.getModelsConfig())

    # Extract metadata
    modelName = args.model or entry.get("model", "")
    if not modelName:
        print("Error: No model specified and no model found in log entry", file=sys.stderr)
        return 1

    print(f"Model: {_col(modelName, _ANSI_BLUE)}")
    if "date" in entry:
        print(f"Original date: {entry['date']}")
    if "status" in entry:
        print(f"Original status: {entry['status']}")
    print()

    # Reconstruct messages
    if "request" not in entry or not isinstance(entry["request"], list):
        print("Error: Log entry missing 'request' field or it is not list of objects", file=sys.stderr)
        return 1

    try:
        messages = ModelMessage.fromDictList(entry["request"])
    except Exception as e:
        print(f"Error reconstructing messages: {_col(str(e), _ANSI_RED)}", file=sys.stderr)
        return 1

    # Get the model
    model = llmManager.getModel(modelName)
    if model is None:
        availableModels = llmManager.listModels()
        print(
            f"Error: Model '{modelName}' not found in configuration.\n",
            file=sys.stderr,
        )
        print(f"Available models: {', '.join(availableModels)}", file=sys.stderr)
        return 1

    # Run the query
    try:
        status, newResponse = await runQueryWithResult(model, modelName, messages)
    except Exception as e:
        print(f"Error running query: {_col(str(e), _ANSI_RED)}", file=sys.stderr)
        logger.exception("Query failed")
        return 1

    # Compare with original response if available
    if "response" in entry and entry["response"]:
        print()
        compareResponses(entry["response"], newResponse)

    return 0 if status == ModelResultStatus.FINAL else 1


if __name__ == "__main__":
    sys.exit(asyncio.run(main()))
