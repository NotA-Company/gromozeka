def loadReadableFile(filePath: str) -> List[Dict[str, Any]]:
    """Load and parse a human-readable JSON or YAML file.

    The file can contain either a single JSON/YAML object (one log entry) or
    a JSON/YAML array (multiple entries). Always returns a list for consistency.
    YAML files support multiline strings with literal block style.

    Args:
        filePath: Path to the human-readable JSON or YAML file.

    Returns:
        List of log entry dictionaries. Each entry contains fields like
        'model', 'request', 'response', 'status', etc.

    Raises:
        FileNotFoundError: If the file doesn't exist.
        json.JSONDecodeError: If the file contains invalid JSON (for .json files).
        yaml.YAMLError: If the file contains invalid YAML (for .yaml/.yml files).
        ValueError: If the file extension is not supported.
    """
    path = Path(filePath)
    if not path.exists():
        raise FileNotFoundError(f"File not found: {filePath}")

    # Determine format from file extension
    suffix = path.suffix.lower()

    if suffix in (".yaml", ".yml"):
        # Load as YAML
        return _loadYamlFile(path)
    elif suffix == ".json":
        # Load as JSON
        return _loadJsonFile(path)
    else:
        raise ValueError(f"Unsupported file format: {suffix}. " "Supported formats: .json, .yaml, .yml")


def _loadYamlFile(path: Path) -> List[Dict[str, Any]]:
    """Load a YAML file containing log entries.

    Args:
        path: Path to the YAML file.

    Returns:
        List of log entry dictionaries.

    Raises:
        yaml.YAMLError: If the file contains invalid YAML.
    """
    with open(path, "r", encoding="utf-8") as f:
        content = f.read()

    # Load YAML (handles single document or multiple documents)
    try:
        data = yaml.safe_load(content)
    except yaml.YAMLError as e:
        raise yaml.YAMLError(f"Failed to parse YAML from {path}: {e}")

    # Normalize to list: handle both single object and array
    if isinstance(data, list):
        return data
    elif isinstance(data, dict):
        return [data]
    else:
        raise ValueError(f"Expected YAML object or array, got {type(data).__name__}")


def _loadJsonFile(path: Path) -> List[Dict[str, Any]]:
    """Load a JSON file containing log entries.

    Args:
        path: Path to the JSON file.

    Returns:
        List of log entry dictionaries.

    Raises:
        json.JSONDecodeError: If the file contains invalid JSON.
    """
    with open(path, "r", encoding="utf-8") as f:
        content = f.read()

    # Handle comment lines (lines starting with #)
    filteredContent = "\n".join(line for line in content.split("\n") if not line.strip().startswith("#"))

    try:
        data = json.loads(filteredContent)
    except json.JSONDecodeError as e:
        raise json.JSONDecodeError(
            f"Failed to parse JSON from {path}: {e.msg}",
            e.doc,
            e.pos,
        )

    # Normalize to list: handle both single object and array
    if isinstance(data, list):
        return data
    elif isinstance(data, dict):
        return [data]
    else:
        raise ValueError(f"Expected JSON object or array, got {type(data).__name__}")


