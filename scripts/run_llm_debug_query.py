#!/usr/bin/env python3
"""Run LLM debug queries from human-readable JSON or YAML logs.

Loads LLM interactions saved by LLM JSON logging, reconstructs the request
messages, and re-runs the query with modifications. Useful for iterating on
system prompts, debugging responses, and testing changes without triggering
the full bot.

Usage:
    ./venv/bin/python3 scripts/run_llm_debug_query.py <readable.json>
    ./venv/bin/python3 scripts/run_llm_debug_query.py <readable.yaml>
    ./venv/bin/python3 scripts/run_llm_debug_query.py <readable.json> --model gpt-4

Exit codes:
    0  Query executed successfully
    1  Error loading file, model not found, or API error
"""

